class Ausleihvorgang {
    constructor(id, benutzerId, startdatum, enddatum, ausleihartikelIds) {
      this.id = id; // Eindeutige Kennung für jeden Ausleihvorgang
      this.benutzerId = benutzerId; // ID des Benutzers, der den Artikel ausleiht
      this.startdatum = startdatum; // Datum, an dem der Artikel ausgeliehen wurde
      this.enddatum = enddatum; // Datum, an dem der Artikel zurückgegeben werden soll
      this.ausleihartikelIds = ausleihartikelIds; // IDs der ausgeliehenen Artikel
    }
  }